    public void tampilDb() {
        Object[] baris = {"Id", "Transaksi", "Jenis Transaksi", "Layanan"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_transaksi.setModel(tabmode);
        try {
            hasil = in.tampilData();
            while (hasil.next()) {
                in.setId(hasil.getString("id"));
                in.setTransaksi(hasil.getString("transaksi"));
                in.setJenis_transaksi(hasil.getString("jenis_transaksi"));
                in.setLayanan(hasil.getString("layanan"));
                String[] data = {
                    in.getId(),
                    in.getTransaksi(),
                    in.getJenis_transaksi(),
                    in.getLayanan(),};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

