    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        if (txt_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Id Belum diisi");
            txt_id.requestFocus();
        } else if (txt_trx.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Transaksi belum diisi");
            txt_trx.requestFocus();
        } else if (txt_jenistrx.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Jenis Transaksi belum diisi");
            txt_jenistrx.requestFocus();
        } else if (txt_layanan.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Layanan belum diisi");
            txt_layanan.requestFocus();
        } else {
            try {
                in.setId(txt_id.getText());
                in.setTransaksi(txt_trx.getText());
                in.setJenis_transaksi(txt_jenistrx.getText());
                in.setLayanan(txt_layanan.getText());

                in.simpanData(in.getId(), in.getTransaksi(), in.getJenis_transaksi(), in.getLayanan());
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                tampilDb();
                reset_text();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "data gagal ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

